var opr = prompt("Operator: ")
var num1= parseInt(prompt("Number1: ")) 
var num2=parseInt(prompt("Number2: ")) 

if(opr == "+")
{
    console.log(num1 + num2)
}
else if(opr == "-")
{
    console.log(num1 - num2)
}
else if(opr == "*")
{
    console.log(num1 * num2)
}
else if(opr == "/")
{
    console.log(num1 / num2)
}